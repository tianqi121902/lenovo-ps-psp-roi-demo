import { formatCurrency, formatPercent } from "../utils/format.js";
import { describeDirectLlmError, generatePitchDirectly, hasDirectLlmConfig } from "../llm/browserProvider.js";

export async function generatePitch(calculation, state) {
  const payload = {
    calculation,
    valueSources: state?.valueSources ?? {}
  };

  if (hasDirectLlmConfig()) {
    try {
      const result = await generatePitchDirectly(payload);
      if (result.pitch) {
        return [
          `Generated by GPT-4o mini (${result.mode}).`,
          "",
          result.pitch
        ].join("\n");
      }
    } catch (error) {
      const description = describeDirectLlmError(error);
      return [
        `Generated by local fallback. GPT pitch failed: ${description.message}`,
        "",
        generateLocalPitch(calculation)
      ].join("\n");
    }
  }

  try {
    const response = await fetch("/api/pitch", {
      method: "POST",
      headers: {
        "content-type": "application/json"
      },
      body: JSON.stringify(payload)
    });

    if (!response.ok) {
      throw new Error(`Pitch request failed: ${response.status}`);
    }

    const payload = await response.json();
    if (payload.pitch) {
      return [
        "Generated by GPT-4o mini (server proxy).",
        "",
        payload.pitch
      ].join("\n");
    }
  } catch {
    // The local fallback keeps the demo usable without a model key.
  }

  return [
    "Generated by local fallback.",
    "",
    generateLocalPitch(calculation)
  ].join("\n");
}

function generateLocalPitch(calculation) {
  const { ps_metrics, psp_metrics } = calculation.metrics;
  const betterOffer =
    psp_metrics.roi_psp >= ps_metrics.roi_ps ? "PSP" : "PS";
  const betterRoi =
    psp_metrics.roi_psp >= ps_metrics.roi_ps ? psp_metrics.roi_psp : ps_metrics.roi_ps;

  return [
    "Sales pitch:",
    `The model shows PS can deliver ${formatCurrency(ps_metrics.net_savings_ps)} in net annual savings with an ROI of ${formatPercent(ps_metrics.roi_ps)}, while PSP can deliver ${formatCurrency(psp_metrics.net_savings_psp)} with an ROI of ${formatPercent(psp_metrics.roi_psp)}.`,
    `${betterOffer} is the stronger financial case in this scenario, with ${formatPercent(betterRoi)} ROI after applying the conservative buffer.`,
    "This gives the customer a clear way to turn support disruption, employee downtime, and onsite dispatch exposure into a predictable annual service investment."
  ].join("\n");
}
